// Handler handles all authentication HTTP routes.
type Handler struct {
	db         *db.DB
	jwtService *JWTService
	logger     *slog.Logger
}

// NewHandler creates a new auth Handler.
func NewHandler(database *db.DB, jwtService *JWTService, logger *slog.Logger) *Handler {
	return &Handler{
		db:         database,
		jwtService: jwtService,
		logger:     logger,
	}
}

