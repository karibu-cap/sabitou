// Register creates a new user account via Connect RPC.
func (h *Handler) Register(ctx context.Context, req *connect.Request[identityv1.RegisterRequest]) (*connect.Response[identityv1.RegisterResponse], error) {
	msg := req.Msg

	email := strings.TrimSpace(strings.ToLower(msg.Email))
	if email == "" || msg.Password == "" || msg.UserName == "" {
		return nil, connect.NewError(connect.CodeInvalidArgument, errors.New("user_name, email and password are required"))
	}

	hash, err := bcrypt.GenerateFromPassword([]byte(msg.Password), bcrypt.DefaultCost)
	if err != nil {
		return nil, connect.NewError(connect.CodeInternal, errors.New("could not hash password"))
	}

	// ref_id is auto-generated by the DB DEFAULT (gen_random_uuid()).
	// The caller may supply one in future by populating the proto field, but
	// for the standard register flow we let the server decide.
	refID := GenerateSmartUserID("USR")
	err = h.db.QueryRow(ctx, `
		INSERT INTO users (ref_id, user_name, email, password_hash)
		VALUES ($1, $2, $3, $4)
		RETURNING ref_id
	`, refID, msg.UserName, email, string(hash)).Scan(&refID)
	if err != nil {
		if strings.Contains(err.Error(), "unique") {
			return nil, connect.NewError(connect.CodeAlreadyExists, errors.New("email or username already registered"))
		}
		h.logger.Error("insert user", "error", err)
		return nil, connect.NewError(connect.CodeInternal, errors.New("registration failed"))
	}

	user, err := h.getUserByRefID(ctx, refID)
	if err != nil {
		return nil, connect.NewError(connect.CodeInternal, errors.New("registration succeeded but user retrieval failed"))
	}

	accessToken, refreshToken, err := h.buildAuthTokens(ctx, user)
	if err != nil {
		return nil, connect.NewError(connect.CodeInternal, errors.New("token generation failed"))
	}

	resp := &identityv1.RegisterResponse{
		Token:        accessToken,
		RefreshToken: refreshToken,
	}
	return connect.NewResponse(resp), nil
}

