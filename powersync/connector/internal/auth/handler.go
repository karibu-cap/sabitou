// RefreshToken issues a new access + refresh token pair, rotating the refresh token.
func (h *Handler) RefreshToken(ctx context.Context, req *connect.Request[identityv1.RefreshTokenRequest]) (*connect.Response[identityv1.RefreshTokenResponse], error) {
	msg := req.Msg

	if msg.RefreshToken == "" {
		return nil, connect.NewError(connect.CodeInvalidArgument, errors.New("refresh_token is required"))
	}

	tokenHash := hashToken(msg.RefreshToken)

	var userID string
	err := h.db.QueryRow(ctx, `
		SELECT user_id FROM refresh_tokens
		WHERE token_hash = $1 AND revoked = FALSE AND expires_at > NOW()
	`, tokenHash).Scan(&userID)
	if err != nil {
		return nil, connect.NewError(connect.CodeUnauthenticated, errors.New("refresh token is invalid or expired"))
	}

	// Revoke used token (rotation prevents replay attacks).
	if _, err := h.db.Exec(ctx, `
		UPDATE refresh_tokens SET revoked = TRUE WHERE token_hash = $1
	`, tokenHash); err != nil {
		h.logger.Error("revoke refresh token", "error", err)
	}

	user, err := h.getUserByRefID(ctx, userID)
	if err != nil || user == nil {
		return nil, connect.NewError(connect.CodeUnauthenticated, errors.New("user not found"))
	}

	accessToken, refreshToken, err := h.buildAuthTokens(ctx, user)
	if err != nil {
		h.logger.Error("build auth response", "error", err)
		return nil, connect.NewError(connect.CodeInternal, errors.New("token generation failed"))
	}

	resp := &identityv1.RefreshTokenResponse{
		Token:        accessToken,
		RefreshToken: refreshToken,
	}
	return connect.NewResponse(resp), nil
}

