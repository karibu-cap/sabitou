// Login authenticates a user with email/phone/username + password and returns tokens.
func (h *Handler) Login(ctx context.Context, req *connect.Request[identityv1.LoginRequest]) (*connect.Response[identityv1.LoginResponse], error) {
	msg := req.Msg

	var emailOrPhoneOrUser string
	if msg.Email != nil {
		emailOrPhoneOrUser = *msg.Email
	} else if msg.PhoneNumber != nil {
		emailOrPhoneOrUser = *msg.PhoneNumber
	} else if msg.UserName != nil {
		emailOrPhoneOrUser = *msg.UserName
	}

	emailOrPhoneOrUser = strings.TrimSpace(strings.ToLower(emailOrPhoneOrUser))
	if emailOrPhoneOrUser == "" || msg.Password == "" {
		h.logger.Error("missing credentials", "identifier", emailOrPhoneOrUser)
		return nil, connect.NewError(connect.CodeInvalidArgument, errors.New("missing credentials"))
	}

	user, err := h.getUserByEmailOrPhoneOrUser(ctx, emailOrPhoneOrUser)
	if err != nil || user == nil {
		h.logger.Error("user not found", "error", err, "identifier", emailOrPhoneOrUser)
		// Constant-time response to prevent user enumeration.
		bcrypt.CompareHashAndPassword([]byte("$2a$12$dummy"), []byte(msg.Password)) //nolint:errcheck
		return nil, connect.NewError(connect.CodeUnauthenticated, errors.New("invalid credentials"))
	}

	passwordHash, err := h.getPasswordHash(ctx, *user.RefId)
	if err != nil {
		h.logger.Error("failed to get password hash", "error", err, "ref_id", user.RefId)
		return nil, connect.NewError(connect.CodeUnauthenticated, errors.New("invalid credentials"))
	}

	if err := bcrypt.CompareHashAndPassword([]byte(passwordHash), []byte(msg.Password)); err != nil {
		h.logger.Error("password mismatch", "error", err, "ref_id", user.RefId)
		return nil, connect.NewError(connect.CodeUnauthenticated, errors.New("invalid credentials"))
	}

	accessToken, refreshToken, err := h.buildAuthTokens(ctx, user)
	if err != nil {
		h.logger.Error("build auth tokens failed", "error", err)
		return nil, connect.NewError(connect.CodeInternal, errors.New("authentication failed"))
	}

	resp := &identityv1.LoginResponse{
		Token:        accessToken,
		RefreshToken: refreshToken,
	}
	return connect.NewResponse(resp), nil
}

